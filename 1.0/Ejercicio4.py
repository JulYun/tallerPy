# Definir la función para la división
def division(a, b):
    return a / b

# Definir la función para la suma
def suma(a, b):
    return a + b

# Imprimir detalles sobre la operación de división
print("La división de dos números a y b")
print("Sean 'a' y 'b' dos números")
print("donde a = 1 y b = 2, entonces a/b es")
print(division(1, 2))

# Imprimir detalles sobre la operación de suma
print("\nLa suma de dos números a y b")
print("Sean 'a' y 'b' dos números")
print("donde a = 10 y b = 2, entonces a + b es")
print(suma(10, 2))
