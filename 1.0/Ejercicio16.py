# Definir la variable pelicula
pelicula = "Casino Royale"

# Imprimir la cadena completa
print(pelicula)

# Imprimir la longitud de la cadena
print(len(pelicula))

# Imprimir el primer carácter de la cadena
print(pelicula[0])

# Imprimir los primeros 6 caracteres de la cadena
print(pelicula[0:6])

# Imprimir la cadena en mayúsculas
print(pelicula.upper())

# Imprimir la cadena en minúsculas
print(pelicula.lower())

# Iterar y imprimir cada carácter de la cadena
for caracter in pelicula:
   print("- " + caracter)
